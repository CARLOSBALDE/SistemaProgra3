    private void BtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGuardarActionPerformed
        
   
     if(ValidarVacios()){
          JOptionPane.showMessageDialog(null, "Rellena todos los campos");
      }else{
          PanelNuevoAlumno.setVisible(false);
            String[] c = new String[4];
            
            c[0]=Txtid.getText();
            c[1]=Txtalumnonuevo.getText();
            c[2]=Txtcorreo.getText();
            c[3]=txtedad.getText();
            
          Lista elementos = new Lista(c[0],c[1] ,c[2] ,c[3] );
           
           lista.add(elementos);
        
          modelo.addRow(c);
               
      }
         VaciarCampos();
      
    }//GEN-LAST:event_BtnGuardarActionPerformed
    private void VaciarCampos(){
        TxtBuscar.setText("");
        Txtalumnonuevo.setText("");
        Txtcorreo.setText("");
        Txtid.setText("");
        txtedad.setText("");
    }
    private boolean ValidarVacios(){
         if(Txtalumnonuevo.getText().equals("") || Txtcorreo.getText().equals("")|| Txtid.getText().equals("")|| txtedad.getText().equals("")){
         return true;
         }
         return false;
    }
