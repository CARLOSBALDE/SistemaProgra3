    private void BtnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEditarActionPerformed
         
  
        
        if(ValidarVacios()){
              JOptionPane.showMessageDialog(null, "Rellena todos los campos");
        }else{
            
             PanelNuevoAlumno.setVisible(false);
            String[] c = new String[4];
            
            c[0]=Txtid.getText();
            c[1]=Txtalumnonuevo.getText();
            c[2]=Txtcorreo.getText();
            c[3]=txtedad.getText();
            
           lista.get(poscicionEncontrado).setID(c[0]);
           lista.get(poscicionEncontrado).setNombre(c[1]);
           lista.get(poscicionEncontrado).setCorreo(c[2]);
           lista.get(poscicionEncontrado).setEdad(c[3]);
       
          
                
                modelo.setValueAt(c[0], poscicionEncontrado, 0);
                modelo.setValueAt(c[1], poscicionEncontrado, 1);
                modelo.setValueAt(c[2], poscicionEncontrado, 2);
                modelo.setValueAt(c[3], poscicionEncontrado, 3);
                
          
          
          
            
        }
        
              VaciarCampos();
    }//GEN-LAST:event_BtnEditarActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       Existe=false;
        
        int  id =Integer.parseInt(TxtBuscar.getText()) ;
        
        for (int i = 0; i < lista.size(); i++) {
            
            if(id==Integer.parseInt(lista.get(i).getID())){
                Existe=true; 
                JOptionPane.showMessageDialog(null,"Encontrado"); 
                PanelNuevoAlumno.setVisible(true);
                BtnGuardar.setVisible(false);
                BtnEditar.setVisible(true);
                BtnEliminar.setVisible(true);
                
                Txtalumnonuevo.setText(lista.get(i).getNombre());
                Txtcorreo.setText(lista.get(i).getCorreo());
                Txtid.setText(lista.get(i).getID());
                txtedad.setText(lista.get(i).getEdad());
                poscicionEncontrado=i;
                break;
            }
            
        }
        
        if(!Existe){
            JOptionPane.showMessageDialog(null, "No encontrado");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        
        PanelNuevoAlumno.setVisible(false);
        lista.remove(poscicionEncontrado);
        modelo.removeRow(poscicionEncontrado);
        VaciarCampos();
        
    }//GEN-LAST:event_BtnEliminarActionPerformed
    private void Consumirletras(java.awt.event.KeyEvent evt){
         int Carac = (int)  evt.getKeyChar();
    
        if(Carac<48 || Carac>57){
            evt.consume();
            Toolkit.getDefaultToolkit().beep();
        }
    }
    private void ConsumirNums(java.awt.event.KeyEvent evt){
        
        int Carac = (int)  evt.getKeyChar();
    
        if(Carac>=48 && Carac<=57){
            evt.consume();
            Toolkit.getDefaultToolkit().beep();
        }
        
    }

